    private void bajaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bajaBtnActionPerformed
        // TODO add your handling code here:
        // VERIFICA QUE NO ESTE VACIO EL ESPACIO DEL CODIGO
        if(this.bajaF.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Favor de teclear el ID del cliente a eliminar","Faltan Datos",JOptionPane.INFORMATION_MESSAGE);
        }
         else if(this.listaTipoB.getSelectedValue() == null)
            JOptionPane.showMessageDialog(null, "No se selecciono que tipo de usuario sera eliminado","Faltan Datos",JOptionPane.INFORMATION_MESSAGE);
         else // SI NO ESTA VACIO Y DESELECCIONADA LA LISTA
        {
             // GUARDA LOS DATOS
        int id = Integer.parseInt(this.bajaF.getText());
        int tipo = this.listaTipoB.getSelectedIndex() + 1;
        
        try {
            int r = this.registroAdmin.delete(id, tipo); // BORRA EL USUARIO
            if(r == 0) // SI NO FUE EXITOSO LO NOTIFICA CON UNA VENTANA DE NOTIFICACIONES
                 JOptionPane.showMessageDialog(null, "No se encontro al usuario con el ID\n" + id,"No se encontro ID",JOptionPane.ERROR_MESSAGE);
            
        } catch (Exception ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        // ACTUALIZA LISTAS
        try {
            this.listaUsuarios.setModel(this.registroAdmin.actualizarJList());
        } catch (IOException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            
        }// RESETEA LA SECCCION DE BAJA
            this.bajaF.setText("");
            this.listaTipoB.clearSelection();
        }
        
    }//GEN-LAST:event_bajaBtnActionPerformed
