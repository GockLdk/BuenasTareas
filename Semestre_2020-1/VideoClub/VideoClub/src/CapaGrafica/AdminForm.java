    private void consultaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaBtnActionPerformed
        // TODO add your handling code here:
        if(this.consultaF.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Favor de ingresar el codigo de la producto a buscar","Faltan Datos",JOptionPane.INFORMATION_MESSAGE);
        else if (this.listaTipoPConsulta.getSelectedValue() == null)
        {
            JOptionPane.showMessageDialog(null, "No se selecciono el tipo de producto a buscar","Faltan Datos",JOptionPane.INFORMATION_MESSAGE);
        }
        else // SI TODO ESTA LLENADO
        {
            // GUARDA DATOS
            int consul = Integer.parseInt(this.consultaF.getText());
            int tipo = this.listaTipoPConsulta.getSelectedIndex() + 1;
         
            try {
                Producto pro = this.registroAdminPro.bus(consul, tipo); // hACE UNA BUSQUEDA EN LAS CARPETAS STOCK
                 try {
                Producto proA = this.registroAdminPro.busAlquiler(consul, tipo); // HACE UNA BUSQUEDA EN LA CARPETA ALQUILER
                if(pro != null && proA != null) // SI SI ENCUENTRA AMBAS SE COMBINAN DATOS PARA MOSTRARLOS
                {
                    
                    String info = this.listaTipoPConsulta.getSelectedValue() + "\nCodigo: " + pro.getCodigo() + "\nNombre: " +  pro.getNombre() + "\nA√±o: " + pro.getCreacion() + "\nStock:  " + pro.getExistencia() +"\nAlquiler: " + proA.getExistencia() + "\nTotal: " + (pro.getExistencia()+proA.getExistencia());
                    JOptionPane.showMessageDialog(null, info,"Resultado de la busqueda",JOptionPane.PLAIN_MESSAGE);
                }
                else if(pro != null && proA == null) // EN CASO DE ENCONTRAR EN sTOCK MUESTRA SOLO DATOS DE aLQUILER
                {
                    String info = this.listaTipoPConsulta.getSelectedValue() + "\nCodigo: " + pro.getCodigo() + "\nNombre: " +  pro.getNombre() + "\nA√±o: " + pro.getCreacion() + "\nStock:  " + pro.getExistencia() +"\nAlquiler: 0 \nTotal: " + (pro.getExistencia());
                    JOptionPane.showMessageDialog(null, info,"Resultado de la busqueda",JOptionPane.PLAIN_MESSAGE);
                }
                else if(pro == null && proA != null) // LO MISMO CUANDO NO ENCUENTRA EN ALQUILER
                {
                    String info = this.listaTipoPConsulta.getSelectedValue() + "\nCodigo: " + proA.getCodigo() + "\nNombre: " +  proA.getNombre() + "\nA√±o: " + proA.getCreacion() + "\nStock: 0 \nAlquiler:" + proA.getExistencia() +  "\nTotal: " + (proA.getExistencia());
                    JOptionPane.showMessageDialog(null, info,"Resultado de la busqueda",JOptionPane.PLAIN_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Producto No Existente","Resultado de la busqueda",JOptionPane.ERROR_MESSAGE);
                }
                
                // RESETEO
                this.listaTipoPConsulta.clearSelection();
                this.consultaF.setText("");
            } catch (Exception ex) {
                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            } catch (Exception ex) {
                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
           
            
        }
        
    }//GEN-LAST:event_consultaBtnActionPerformed
        
