    private void registrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarBtnActionPerformed
        // TODO add your handling code here:
        // VERIFICA QUE TODO ESTE LLENADO Y SELECCIONADO
        if (this.nomFRes.getText().isEmpty() || this.nomFRes1.getText().isEmpty() || this.nomFRes2.getText().isEmpty() || this.nomFRes3.getText().isEmpty() || this.nomFRes4.getText().isEmpty()  )
            JOptionPane.showMessageDialog(null, "Falta de llenar algun espacio","Faltan Datos",JOptionPane.INFORMATION_MESSAGE);
        else if(this.listaTipo.getSelectedValue() == null)
            JOptionPane.showMessageDialog(null, "No se seleccion que tipo de usuario sera","Faltan Datos",JOptionPane.INFORMATION_MESSAGE);
        else
        {
            // GUARDA LOS VALORES
        String nom = this.nomFRes.getText();
        int contrasena = Integer.parseInt(this.nomFRes4.getText());
        int ed = Integer.parseInt(this.nomFRes1.getText());
        int tel = Integer.parseInt(this.nomFRes2.getText());
        String mail = this.nomFRes3.getText();
        int tipo = this.listaTipo.getSelectedIndex() + 1;
        // EGNERA UN NUMERO ALEATORIO DE 9 DIGITOS PARA EL ID DEL USUARIO
        Random r = new Random();
        int id = r.nextInt((999999999 - 100000000) + 1) + 100000000;
        // CREA AL USUARIO
        Usuario user = new Usuario(nom,id,contrasena,ed,tel,mail,tipo);
        //System.out.println(nom +"\n" + ed +"\n" + tel +"\n" + mail +"\n" + tipo +"\n"  );
        
        try {
            this.registroAdmin.insert(user); // LOS REGISTRA EN  SU CARPETA CORRESPONDIENTE
        } catch (IOException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        // ACTUALIZA LAS LISTAS PARA VER LOS CAMBIOS RECIENTES
        try {
            this.listaUsuarios.setModel(this.registroAdmin.actualizarJList());
        } catch (IOException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        // PONE EN BLANCO LOS ESPACIO A LLENAR Y DESELECCIONA LA JLIST
        this.nomFRes.setText("");
        this.nomFRes1.setText("");
        this.nomFRes2.setText("");
        this.nomFRes3.setText("");
        this.nomFRes4.setText("");
        this.listaTipo.clearSelection();
        }
    }//GEN-LAST:event_registrarBtnActionPerformed

